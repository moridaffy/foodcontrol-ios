import UIKit

extension UIColor {
  static var additionalYellow: UIColor {
    return UIColor(red: 0.997, green: 0.741, blue: 0.187, alpha: 1.00)    // #FEBD30
  }
  
  static var additionalGreen: UIColor {
    return UIColor(red: 0.216, green: 0.772, blue: 0.213, alpha: 1.00)    // #37C536
  }
  
  static var additionalRed: UIColor {
    return UIColor(red: 0.878, green: 0.255, blue: 0.255, alpha: 1.00)    // #E04141
  }
  
  static var additionalGrayDark: UIColor {
    return UIColor(red: 0.498, green: 0.498, blue: 0.498, alpha: 1.00)    // #7F7F7F
  }
  
  static var additionalGray: UIColor {
    return UIColor(red: 0.741, green: 0.741, blue: 0.741, alpha: 1.00)    // #BDBDBD
  }
  
  static var additionalGrayLight: UIColor {
    return UIColor(red: 0.875, green: 0.875, blue: 0.875, alpha: 1.00)    // #DFDFDF
  }
}
